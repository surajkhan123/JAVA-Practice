// package Operators.Bitwise;

public class B {
  public static void main(String[] args) {
    int a = 38;
    int b = 47;
  
    int c = a&b;
    System.out.println(c); // 38

    int d = a|b;
    System.out.println(d); // 47

    int e = a ^b;
    System.out.println(e); // 9

    int f = ~a;
    System.out.println(f);

  }
}
